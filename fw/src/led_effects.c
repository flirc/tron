static uint8_t brightness[12];
static volatile uint8_t inner = 0x00;
static volatile uint16_t outer = 0xFFBE;

static unsigned char ticks_i = 0;
static unsigned char ticks_o = 0;
static uint8_t pos = 0;

void wave_init(void)
{
	int i;
  	for(i = 0; i < 12; i++) {
    		brightness[i] = (255/6)*i;
  	}
}

void glow_brightness(void)
{
	pos++;
}

void inner_glow_pwm(void)
{
	ticks_i++;
	inner = 0x00;

	if(ticks_i >= pgm_read_byte(&cosine[pos]))
		inner |= 0xFF;

	output_leds(inner, outer);
}

void wave_brightness(void)
{
	uint8_t i;

	for(i = 0; i < 12; i++) {
		brightness[i] += 1;
	}
}

void inner_wave_pwm(void)
{
	uint8_t i;
	ticks_i++;

	inner = 0x00;

	for (i = 0; i < 8; i++) {
		if(ticks_i >= pgm_read_byte(&cosine[brightness[i]]))
			inner |= 1<<i;
	}

	output_leds(inner, outer);
}

void outer_wave_pwm(void)
{
	uint8_t i;
	ticks_o++;

	outer = 0x00;

	for (i = 0; i < 12; i++) {
		if(ticks_o >= pgm_read_byte(&cosine[brightness[i]]))
			outer |= 1<<i;
	}

	output_leds(inner, outer);
}

void outer_glow_pwm(void)
{
	ticks_i++;
	outer = 0x00;

	if(ticks_i >= pgm_read_byte(&cosine[pos]))
		outer |= 0xFFFF;

	output_leds(inner, outer);
}

void outer_glow_ph_pwm(void)
{
	ticks_i++;
	outer = 0x00;

	if(ticks_i >= pgm_read_byte(&cosine[(uint8_t)(pos+128)]))
		outer |= 0xFFFF;

	output_leds(inner, outer);
}

void reset_outer_ring_spin(void)
{
	outer = 0xFFBE;
}

void outer_ring_spin(void)
{
        int carry;
        carry = outer&0x1;
        outer = outer>>1;
        if (carry)
		outer |= 1<<11;

        output_leds(inner, outer);
}
